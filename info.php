      <p>
        The Daytona 24 Hours is a <strong>multi-class endurance race</strong>, typically featuring around <strong>20–25 DMAX karts</strong> and a similar number of <strong>Sodi karts</strong> on track at once.  
        This mix makes awareness and consistency far more important than outright pace.
      </p>
      <p>
        The goal isn't to be the fastest driver on lap one — it's to be <strong>consistent first and fast second</strong>.  
        Avoid contact, stay calm, and focus on bringing the kart home in one piece. The race is often a battle against yourself as much as the drivers around you.
      </p>
      <p>
        <strong>Fatigue is the real enemy</strong>. Managing energy, focus and concentration over a full 24 hours is what separates a strong finish from an early retirement.
      </p>
